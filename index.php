<p><span class="lite1">Thérapie officiellement</span> reconnue au Japon comme une médecine à part entière, le Ministère de la Santé Japonais le définit ainsi :</p>
<blockquote>
	<p>  "Le shiatsu est une forme de manipulation exercée à l'aide des pouces,
	des doigts et des paumes, sans recours à aucun instrument mécanique ou autre,
	par application d'une pression au niveau de l'épiderme,
	afin de corriger les dysfonctionnements internes, d'améliorer et de préserver la santé."</p>
</blockquote>
<p>Le shiatsu joue dans le domaine de la santé japonaise un rôle comparable et complémentaire à ceux de la kinésithérapie en France, des Heilpraktiker en Allemagne ou de l’ostéopathie aux USA.</p>
<p>En Europe, le <span class="lite1">Shiatsu</span> est une technique thérapeutique et préventive de plus en plus répandue, reconnue (Italie, Allemagne, Royaume-Uni, Belgique, Pays Bas, pays nordiques), voir remboursée par les assurances santé. Le <strong>Parlement Européen</strong>, en 1997, l'a reconnu comme médecine non conventionnelle digne d’intérêt,
au même titre que l'acupuncture, l'ostéopathie et la chiropractie.</p>
