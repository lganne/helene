      <p><span class="dropcap rose">L</span>e Shiatsu ("Shi" = pression, "atsu" = doigt), est une méthode manuelle japonaise, basée sur les principes de la <strong>Médecine Traditionnelle Chinoise</strong>, qui allie pressions, étirements des membres et mobilisation des articulations pour influer sur les muscles et les systèmes circulatoires du corps:
